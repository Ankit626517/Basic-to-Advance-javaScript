// 1.Number(): Use to convert string in number
let num = Number("10.33")
console.log((num) + typeof num)  // 10.33

// 2. parseInt(): written number in integer only.
let sum = parseInt("10.33")
console.log(sum)



// 3.parseFloat(): written a number in float only
let number = parseFloat("10.33")
console.log(number + typeof number )