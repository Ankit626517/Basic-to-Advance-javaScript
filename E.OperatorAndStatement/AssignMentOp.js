let a=10;
let b=20;
// 1. Assign operator
console.log(a+b); // 30 a+b=30

// 2. add and Assign
console.log(a+=b); // 30 a+=b=30

// 3. substraction and assign
console.log(a-=b); // -10 a-=b=-10

// 4. multiplication and assign
console.log(a*=b); // 200 a*=b=200

// 5. division and assign
console.log(a/=b); // 0.5 a/=b=0.5

// 6. modulus and assign
console.log(a%=b); // 0 a%=b=0
