let b=20;

// 1. isEqual to 
console.log(a==b); // false

// 2. equal and some type
console.log(a===b); // false

// 3. not equal to
console.log(a!=b); // true

// 4. not identical 
console.log(a!==b); // true

// 5. greater than
console.log(a>b); // false

// 6. greater than equal to 
console.log(a>=b); // false

// 7. less than 
console.log(a<b); // true

// 8. less than equal to 
console.log(a<=b); // true
