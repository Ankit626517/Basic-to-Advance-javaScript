let a = 10;
let b = 5;
// 1. Addition
console.log(a + b); //output is 15

//2.substraction
console.log(a - b); //output is 5
//3.multiplication
console.log(a * b); //output is 50
// 4. division
console.log(a / b); //output is 2

// 5. modulus
console.log(a % b); //output is 0

// 6. increment
console.log(++a); //output is 11
//7. decrement
console.log(--a); //output is 10
